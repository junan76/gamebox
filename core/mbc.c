#define SZ_16KB 16384
#define SZ_8KB 8192
#define SZ_4KB 4096

struct mbc_header {
	uint8_t entry[4];
	uint8_t logo[48];
	uint8_t title[16];
	uint8_t cgb;
	uint8_t license_new;
	uint8_t sgb;
	uint8_t mbc_type;
	uint8_t rom_size;
	uint8_t ram_size;
	uint8_t dest;
	uint8_t license_old;
	uint8_t version;
	uint8_t header_checksum;
	uint8_t global_checksum[2];
};

static uint8_t verify_mbc_header(struct mbc_header *header)
{
	uint8_t checksum = 0;

	uint8_t *start = &header->title[0];
	uint8_t *end = &header->header_checksum;
	for (uint8_t *item = start; item < end; item++) {
		checksum = checksum - *item - 1;
	}

	return checksum == header->header_checksum ? 0 : -1;
}

